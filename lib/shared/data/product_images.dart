String? productImageUrlForName(String productName) {
  final exactMatch = productImages[productName];
  if (exactMatch != null) return exactMatch;

  final normalizedProductName = _normalizeProductName(productName);

  for (final entry in productImages.entries) {
    if (_normalizeProductName(entry.key) == normalizedProductName) {
      return entry.value;
    }
  }

  return null;
}

extension ProductThumbnailImage on ProductModel {
  String? get thumbnailUrl => productImageUrlForName(name);
}

String _normalizeProductName(String value) {
  return value
      .toLowerCase()
      .replaceAll(RegExp(r'\s*\([^)]*\)\s*'), ' ')
      .replaceAll(RegExp(r'[^a-z0-9]+'), ' ')
      .trim()
      .replaceAll(RegExp(r'\s+'), ' ');
}
